// Split text nodes into word spans and highlight each word when it crosses a viewport threshold.
(() => {
  const root = document.querySelector('.scroll-highlight');
  if (!root || !('IntersectionObserver' in window)) return;

  // Convert text nodes into spans(.word) while preserving whitespace and tags.
  const tw = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode: (n) => n.nodeValue.trim().length ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT
  });

  const nodes = [];
  while (tw.nextNode()) nodes.push(tw.currentNode);

  for (const textNode of nodes) {
    const frag = document.createDocumentFragment();
    const parts = textNode.nodeValue.split(/(\s+)/);
    for (const part of parts) {
      if (!part) continue;
      if (/^\s+$/.test(part)) {
        frag.appendChild(document.createTextNode(part));
      } else {
        const span = document.createElement('span');
        span.className = 'word';
        span.textContent = part;
        frag.appendChild(span);
      }
    }
    textNode.parentNode.replaceChild(frag, textNode);
  }

  // Rule: words above the midline are highlighted.
  // For the line intersecting the midline, only highlight the left half of its words.
  // Media (img/svg) use hysteresis to avoid flicker and do not participate in the half-line rule.
  const tokens = Array.from(root.querySelectorAll('.word, img, svg, .pop'));
  let ticking = false;
  const recalc = () => {
    const vh = window.innerHeight || document.documentElement.clientHeight;
    const cutoff = document.getElementById('cuttoff-point');
    const mid = cutoff ? cutoff.getBoundingClientRect().top : (vh / 2);

    const onLine = [];

    for (const el of tokens) {
      const r = el.getBoundingClientRect();
      const top = r.top;
      const bottom = r.bottom;
      if (el.tagName === 'IMG' || el.tagName === 'SVG' || el.classList.contains('pop')) {
        const h = 12; // px hysteresis band for media
        if (bottom < mid - h) {
          el.classList.add('is-on');
        } else if (top > mid + h) {
          el.classList.remove('is-on');
        } else {
          // inside hysteresis band: keep current state
        }
        continue;
      }
      // words
      if (bottom < mid) {
        el.classList.add('is-on');
      } else if (top > mid) {
        el.classList.remove('is-on');
      } else {
        onLine.push({ el, x: r.left + r.width / 2 });
      }
    }

    if (onLine.length) {
      onLine.sort((a, b) => a.x - b.x);
      const half = Math.floor(onLine.length / 2);
      for (let i = 0; i < onLine.length; i++) {
        if (i < half) onLine[i].el.classList.add('is-on');
        else onLine[i].el.classList.remove('is-on');
      }
    }

    ticking = false;
  };
  const onScroll = () => {
    if (!ticking) {
      ticking = true;
      requestAnimationFrame(recalc);
    }
  };

  window.addEventListener('scroll', onScroll, { passive: true });
  window.addEventListener('resize', () => { recalc(); });
  recalc();
})();
</script>

